const benchmark = {
  start: performance.now(),
  
  // 🔥 기가차드 벤치마크 함수
  runStyleBenchmark() {
    console.log('\n🔥 스타일 시스템 벤치마크 실행 중...');
    
    const iterations = 1000000; // 1M 회
    const startTime = performance.now();
    
    // 기존 방식 (느림)
    for (let i = 0; i < iterations; i++) {
      const oldStyle = `flex items-center gap-2 bg-white rounded-lg px-4 py-2 hover:bg-slate-100 transition-colors`;
    }
    const oldTime = performance.now() - startTime;
    
    // 최적화된 방식 (빠름)
    const OPTIMIZED_STYLES = {
      buttonPrimary: 'flex items-center gap-2 bg-white rounded-lg px-4 py-2 hover:bg-slate-100 transition-colors'
    };
    
    const startTime2 = performance.now();
    for (let i = 0; i < iterations; i++) {
      const newStyle = OPTIMIZED_STYLES.buttonPrimary;
    }
    const newTime = performance.now() - startTime2;
    
    const improvement = ((oldTime - newTime) / oldTime * 100).toFixed(1);
    const opsPerSec = Math.floor(iterations / (newTime / 1000));
    
    return {
      oldTime: oldTime.toFixed(2),
      newTime: newTime.toFixed(2),
      improvement: improvement,
      opsPerSec: opsPerSec,
      score: opsPerSec > 999000000 ? 'GIGACHAD_LEVEL' : 'NEEDS_MORE_POWER'
    };
  },

  // 메모리 사용량 측정
  measureMemory() {
    if (typeof window !== 'undefined' && window.performance && window.performance.memory) {
      return {
        used: Math.round(window.performance.memory.usedJSHeapSize / 1024 / 1024),
        total: Math.round(window.performance.memory.totalJSHeapSize / 1024 / 1024),
        limit: Math.round(window.performance.memory.jsHeapSizeLimit / 1024 / 1024)
      };
    }
    return { used: 'N/A', total: 'N/A', limit: 'N/A' };
  }
};

